package com.his.common;

/**
 * 敏感数据脱敏类型枚举
 *
 * <p>定义不同类型敏感数据的脱敏规则
 *
 * <h3>脱敏规则说明</h3>
 * <ul>
 *   <li>PHONE: 手机号脱敏，保留前3位和后4位，中间用*替换（例如：138****1234）</li>
 *   <li>ID_CARD: 身份证号脱敏，保留前6位和后4位，中间用*替换（例如：110101********1234）</li>
 *   <li>ADDRESS: 地址脱敏，详细地址用*替换（例如：北京市朝阳区***）</li>
 *   <li>BANK_CARD: 银行卡号脱敏，保留前4位和后4位，中间用*替换（例如：6222***********1234）</li>
 *   <li>NAME: 姓名脱敏，只保留姓氏（例如：张*）</li>
 * </ul>
 *
 * @author HIS 开发团队
 * @version 1.0
 * @see SensitiveData
 * @see DataMaskingUtils
 */
public enum SensitiveType {

    /**
     * 手机号脱敏
     * <p>规则：保留前3位和后4位
     * <p>示例：13800138000 → 138****8000
     */
    PHONE,

    /**
     * 身份证号脱敏
     * <p>规则：保留前6位和后4位
     * <p>示例：110101199001011234 → 110101********1234
     */
    ID_CARD,

    /**
     * 地址脱敏
     * <p>规则：保留省市区，详细地址用*替换
     * <p>示例：北京市朝阳区建国路88号 → 北京市朝阳区***
     */
    ADDRESS,

    /**
     * 银行卡号脱敏
     * <p>规则：保留前4位和后4位
     * <p>示例：6222021234567890123 → 6222***********0123
     */
    BANK_CARD,

    /**
     * 姓名脱敏
     * <p>规则：只保留姓氏，其余用*替换
     * <p>示例：张三 → 张*；欧阳娜娜 → 欧阳**
     */
    NAME,

    /**
     * 邮箱脱敏
     * <p>规则：保留前2个字符和@后的域名，中间用*替换
     * <p>示例：zhangsan@example.com → zh***@example.com
     */
    EMAIL
}
